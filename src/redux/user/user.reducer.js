const INITIAL_STATE = {
  currentUser: null,
};

// userReducer represent currentUser state in the app, used for login

const userReducer = (state = INITIAL_STATE, action) => {
  switch (action.type) {
    case "SET_CURRENT_USER":
      return {
        ...state,
        currentUser: action.payload,
      };
    default:
      return state;
  }
};

export default userReducer;
